class Validator:
    def __init__(self):
        self.errors = []
        self.warnings = []

    def error(self, msg):
        self.errors.append(f"[ERROR] {msg}")

    def warn(self, msg):
        self.warnings.append(f"[WARN]  {msg}")

    def ok(self) -> bool:
        return len(self.errors) == 0

    def report(self):
        for w in self.warnings:
            print(w, file=sys.stderr)
        for e in self.errors:
            print(e, file=sys.stderr)


def validate(root, v: Validator):
    sensor_ids = set()
    output_ids = set()
    schedule_ids = set()
    rule_ids = set()

    # --- Sensors ---
    for s in root.findall("Sensors/Sensor"):
        sid = int(s.get("id"))
        if sid in sensor_ids:
            v.error(f"Duplicate Sensor id={sid}")
        sensor_ids.add(sid)
        if not (1 <= sid <= 99):
            v.error(f"Sensor id={sid} out of range 1–99")

        mn = float(s.get("min", "-999"))
        mx = float(s.get("max", "9999"))
        if mn >= mx:
            v.error(f"Sensor id={sid}: min({mn}) >= max({mx})")

        t = s.get("type")
        if t not in SENSOR_TYPE_MAP:
            v.error(f"Sensor id={sid}: unknown type '{t}'")

        iface = s.get("interface", "ADC")
        if iface not in IFACE_MAP:
            v.error(f"Sensor id={sid}: unknown interface '{iface}'")

    # --- Outputs ---
    for o in root.findall("Outputs/Output"):
        oid = int(o.get("id"))
        if oid in output_ids:
            v.error(f"Duplicate Output id={oid}")
        output_ids.add(oid)
        if not (1 <= oid <= 99):
            v.error(f"Output id={oid} out of range 1–99")

    # --- Schedules ---
    for sc in root.findall("Schedules/Schedule"):
        scid = int(sc.get("id"))
        if scid in schedule_ids:
            v.error(f"Duplicate Schedule id={scid}")
        schedule_ids.add(scid)
        slots = sc.findall("Slot")
        if len(slots) > 8:
            v.error(f"Schedule id={scid}: more than 8 slots ({len(slots)})")

    # --- Rules ---
    for r in root.findall("Rules/Rule"):
        rid = int(r.get("id"))
        if rid in rule_ids:
            v.error(f"Duplicate Rule id={rid}")
        rule_ids.add(rid)

        conds = r.findall("Conditions/Condition")
        if len(conds) == 0:
            v.error(f"Rule id={rid}: no conditions defined")
        if len(conds) > 8:
            v.error(f"Rule id={rid}: too many conditions ({len(conds)}, max 8)")

        acts = r.findall("Actions/Action")
        if len(acts) == 0:
            v.error(f"Rule id={rid}: no actions defined")
        if len(acts) > 8:
            v.error(f"Rule id={rid}: too many actions ({len(acts)}, max 8)")

        # Check sensor/output refs exist
        for c in conds:
            csid = int(c.get("sensor_id"))
            if csid not in sensor_ids:
                v.error(f"Rule id={rid}: Condition references undefined Sensor id={csid}")
            op = c.get("op")
            if op not in OP_MAP:
                v.error(f"Rule id={rid}: unknown operator '{op}'")
            if op in ("BETWEEN", "NOT_BETWEEN"):
                if c.get("value_lo") is None or c.get("value_hi") is None:
                    v.error(f"Rule id={rid}: BETWEEN/NOT_BETWEEN requires value_lo and value_hi")

        for a in acts:
            aoid = int(a.get("output_id"))
            if aoid not in output_ids:
                v.error(f"Rule id={rid}: Action references undefined Output id={aoid}")

        sched_id = int(r.get("schedule_id", "0"))
        if sched_id != 0 and sched_id not in schedule_ids:
            v.error(f"Rule id={rid}: schedule_id={sched_id} not defined")

        logic = r.get("logic", "AND")
        if logic not in LOGIC_MAP:
            v.error(f"Rule id={rid}: unknown logic '{logic}'")

        prio = int(r.get("priority", "50"))
        if not (0 <= prio <= 255):
            v.error(f"Rule id={rid}: priority {prio} out of range 0–255")


