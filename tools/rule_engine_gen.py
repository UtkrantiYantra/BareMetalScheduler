def main():
    parser = argparse.ArgumentParser(description="Rule Engine C Code Generator")
    parser.add_argument("--config", required=True, help="Path to XML config file")
    parser.add_argument("--out",    default="generated", help="Output directory")
    parser.add_argument("--no-validate", action="store_true",
                        help="Skip XML validation")
    args = parser.parse_args()

    if not os.path.isfile(args.config):
        print(f"[ERROR] Config file not found: {args.config}", file=sys.stderr)
        sys.exit(1)

    tree = ET.parse(args.config)
    root = tree.getroot()

    # Validation
    if not args.no_validate:
        v = Validator()
        validate(root, v)
        v.report()
        if not v.ok():
            print(f"\n[FAIL] {len(v.errors)} validation error(s). Aborting.", file=sys.stderr)
            sys.exit(2)
        else:
            print(f"[OK] Validation passed ({len(v.warnings)} warning(s))")

    os.makedirs(args.out, exist_ok=True)

    files = {
        "config.h":       gen_config_h(root, args.config),
        "config.c":       gen_config_c(root, args.config),
        "rule_table.h":   gen_rule_table_h(root, args.config),
        "rule_table.c":   gen_rule_table_c(root, args.config),
    }

    for fname, content in files.items():
        path = os.path.join(args.out, fname)
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"[GEN] {path}  ({len(content):,} bytes)")

    print(f"\n[DONE] Generated {len(files)} files in '{args.out}/'")


if __name__ == "__main__":
    main()
