def gen_rule_table_c(root, src_file: str) -> str:
    sensors   = root.findall("Sensors/Sensor")
    outputs   = root.findall("Outputs/Output")
    rules     = root.findall("Rules/Rule")
    schedules = root.findall("Schedules/Schedule") or []
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    out = []
    out.append(HEADER_COMMENT.format(ts=ts, src=src_file))
    out.append('#include "rule_table.h"\n')
    out.append('#include "re_engine.h"\n\n')

    # ---- Sensor table ----
    out.append(banner("Sensor descriptor table"))
    out.append(f"const SensorDescriptor_t g_sensor_table[{len(sensors)}U] = {{\n")
    for s in sensors:
        name     = c_str(s.get("name"))
        unit     = c_str(s.get("unit", ""))
        stype    = SENSOR_TYPE_MAP.get(s.get("type"), "SENSOR_TYPE_DIGITAL")
        mn       = float_to_scaled(s.get("min", "-99900"))
        mx       = float_to_scaled(s.get("max", "999900"))
        thi      = float_to_scaled(s.get("threshold_hi", s.get("max", "999900")))
        tlo      = float_to_scaled(s.get("threshold_lo", s.get("min", "-99900")))
        hyst     = float_to_scaled(s.get("hysteresis", "0"))
        interval = int(s.get("sample_interval_ms", "1000"))
        enabled  = "true" if s.get("enabled", "true").lower() == "true" else "false"

        out.append(
            f"    /* Sensor {s.get('id')} — {name} */\n"
            f"    {{\n"
            f"        .id                 = {s.get('id')}U,\n"
            f"        .name               = \"{name}\",\n"
            f"        .type               = {stype},\n"
            f"        .unit               = \"{unit}\",\n"
            f"        .min_value          = {mn}L,\n"
            f"        .max_value          = {mx}L,\n"
            f"        .threshold_hi       = {thi}L,\n"
            f"        .threshold_lo       = {tlo}L,\n"
            f"        .hysteresis         = {hyst}L,\n"
            f"        .sample_interval_ms = {interval}U,\n"
            f"        .enabled            = {enabled}\n"
            f"    }},\n"
        )
    out.append("};\n\n")

    # ---- Output table ----
    out.append(banner("Output descriptor table"))
    out.append(f"const OutputDescriptor_t g_output_table[{len(outputs)}U] = {{\n")
    for o in outputs:
        name     = c_str(o.get("name"))
        otype    = OUTPUT_TYPE_MAP.get(o.get("type"), "OUTPUT_TYPE_RELAY")
        prio     = int(o.get("priority", "50"))
        defst    = "1U" if o.get("default_state", "OFF").upper() == "ON" else "0U"
        fsst     = "1U" if o.get("failsafe_state", "OFF").upper() == "ON" else "0U"

        out.append(
            f"    /* Output {o.get('id')} — {name} */\n"
            f"    {{\n"
            f"        .id            = {o.get('id')}U,\n"
            f"        .name          = \"{name}\",\n"
            f"        .type          = {otype},\n"
            f"        .priority      = {prio}U,\n"
            f"        .default_state = {defst},\n"
            f"        .failsafe_state = {fsst}\n"
            f"    }},\n"
        )
    out.append("};\n\n")

    # ---- Schedule table ----
    out.append(banner("Schedule descriptor table (TOU)"))
    out.append(f"const ScheduleDescriptor_t g_schedule_table[{max(len(schedules), 1)}U] = {{\n")
    if not schedules:
        out.append("    { 0 }  /* No schedules defined */\n")
    for sc in schedules:
        name    = c_str(sc.get("name"))
        sctype  = SCHED_TYPE_MAP.get(sc.get("type", "DAILY"), "SCHED_DAILY")
        enabled = "true" if sc.get("enabled", "true").lower() == "true" else "false"
        slots   = sc.findall("Slot")

        out.append(
            f"    /* Schedule {sc.get('id')} — {name} */\n"
            f"    {{\n"
            f"        .id        = {sc.get('id')}U,\n"
            f"        .name      = \"{name}\",\n"
            f"        .type      = {sctype},\n"
            f"        .num_slots = {len(slots)}U,\n"
            f"        .slots     = {{\n"
        )
        for slot in slots:
            sh, sm = time_to_hm(slot.get("start"))
            eh, em = time_to_hm(slot.get("end"))
            mask   = weekday_mask(slot.get("weekday_mask", "1111111"))
            out.append(
                f"            {{ .start_hour={sh}U, .start_min={sm}U, "
                f".end_hour={eh}U, .end_min={em}U, .weekday_mask=0x{mask:02X}U }},\n"
            )
        out.append(
            f"        }},\n"
            f"        .enabled = {enabled}\n"
            f"    }},\n"
        )
    out.append("};\n\n")

    # ---- Rule table ----
    out.append(banner("Rule descriptor table"))
    out.append(f"const RuleDescriptor_t g_rule_table[{len(rules)}U] = {{\n")
    for r in rules:
        name      = c_str(r.get("name"))
        enabled   = "true" if r.get("enabled", "true").lower() == "true" else "false"
        logic     = LOGIC_MAP.get(r.get("logic", "AND"), "LOGIC_AND")
        on_delay  = int(r.get("on_delay_ms", "0"))
        off_delay = int(r.get("off_delay_ms", "0"))
        sched_id  = int(r.get("schedule_id", "0"))
        prio      = int(r.get("priority", "50"))

        conds = r.findall("Conditions/Condition")
        acts  = r.findall("Actions/Action")

        out.append(
            f"    /* Rule {r.get('id')} — {name} */\n"
            f"    {{\n"
            f"        .id               = {r.get('id')}U,\n"
            f"        .name             = \"{name}\",\n"
            f"        .enabled          = {enabled},\n"
            f"        .num_conditions   = {len(conds)}U,\n"
            f"        .conditions       = {{\n"
        )

        for ci, c in enumerate(conds):
            op = OP_MAP.get(c.get("op"), "OP_EQ")
            if c.get("op") in ("BETWEEN", "NOT_BETWEEN"):
                va = float_to_scaled(c.get("value_lo", "0"))
                vb = float_to_scaled(c.get("value_hi", "0"))
            elif c.get("op") in ("ON", "OFF"):
                va, vb = 0, 0
            else:
                va = float_to_scaled(c.get("value", "0"))
                vb = 0

            out.append(
                f"            [{ci}] = {{ .sensor_id={c.get('sensor_id')}U, "
                f".op={op}, .value_a={va}L, .value_b={vb}L }},\n"
            )

        out.append(
            f"        }},\n"
            f"        .condition_logic  = {logic},\n"
            f"        .on_delay_ms      = {on_delay}U,\n"
            f"        .off_delay_ms     = {off_delay}U,\n"
            f"        .schedule_id      = {sched_id}U,\n"
            f"        .num_actions      = {len(acts)}U,\n"
            f"        .actions          = {{\n"
        )

        for ai, a in enumerate(acts):
            act_type    = ACTION_MAP.get(a.get("state", "ON"), "ACTION_ON")
            duration_ms = int(a.get("duration_ms", "0"))
            out.append(
                f"            [{ai}] = {{ .output_id={a.get('output_id')}U, "
                f".action={act_type}, .duration_ms={duration_ms}U }},\n"
            )

        out.append(
            f"        }},\n"
            f"        .priority         = {prio}U\n"
            f"    }},\n"
        )

    out.append("};\n\n")

    # Count constants
    out.append(banner("Count constants (const so usable in expressions)"))
    out.append(f"const uint16_t RE_NUM_SENSORS   = {len(sensors)}U;\n")
    out.append(f"const uint16_t RE_NUM_OUTPUTS   = {len(outputs)}U;\n")
    out.append(f"const uint16_t RE_NUM_RULES     = {len(rules)}U;\n")
    out.append(f"const uint16_t RE_NUM_SCHEDULES = {max(len(schedules), 1)}U;\n")

    return "".join(out)


