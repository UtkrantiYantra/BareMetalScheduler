def gen_config_h(root, src_file: str) -> str:
    sensors   = root.findall("Sensors/Sensor")
    outputs   = root.findall("Outputs/Output")
    rules     = root.findall("Rules/Rule")
    schedules = root.findall("Schedules/Schedule") or []

    ts  = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    out = []
    out.append(HEADER_COMMENT.format(ts=ts, src=src_file))
    out.append("#ifndef CONFIG_H\n#define CONFIG_H\n")
    out.append('#include "re_types.h"\n')
    out.append(banner("Count constants"))
    out.append(f"#define RE_NUM_SENSORS    ({len(sensors)}U)\n")
    out.append(f"#define RE_NUM_OUTPUTS    ({len(outputs)}U)\n")
    out.append(f"#define RE_NUM_RULES      ({len(rules)}U)\n")
    out.append(f"#define RE_NUM_SCHEDULES  ({len(schedules)}U)\n\n")

    out.append(banner("Sensor ID macros"))
    for s in sensors:
        name = re.sub(r'\W+', '_', s.get("name")).upper()
        out.append(f"#define SENSOR_ID_{name:<30s} ({s.get('id')}U)\n")

    out.append("\n")
    out.append(banner("Output ID macros"))
    for o in outputs:
        name = re.sub(r'\W+', '_', o.get("name")).upper()
        out.append(f"#define OUTPUT_ID_{name:<30s} ({o.get('id')}U)\n")

    out.append("\n")
    out.append(banner("Sensor interface type enum (used by HAL dispatch)"))
    out.append(
        "typedef enum {\n"
        "    RE_SENSOR_IF_ADC     = 0,\n"
        "    RE_SENSOR_IF_GPIO    = 1,\n"
        "    RE_SENSOR_IF_I2C     = 2,\n"
        "    RE_SENSOR_IF_SPI     = 3,\n"
        "    RE_SENSOR_IF_VIRTUAL = 4\n"
        "} RE_SensorInterface_t;\n\n"
    )

    out.append(banner("HAL lookup function prototypes (implemented in config.c)"))
    out.append("RE_SensorInterface_t RE_SensorGetInterface(uint8_t sensor_id);\n")
    out.append("int                  RE_SensorGetADCChannel(uint8_t sensor_id);\n")
    out.append("int                  RE_SensorGetGPIOPin(uint8_t sensor_id);\n")
    out.append("uint32_t             RE_SensorGetScale(uint8_t sensor_id);\n")
    out.append("void*                RE_SensorGetGPIOPort(uint8_t sensor_id);\n")
    out.append("int                  RE_OutputGetGPIOPin(uint8_t output_id);\n")
    out.append("void*                RE_OutputGetGPIOPort(uint8_t output_id);\n\n")

    out.append("#endif /* CONFIG_H */\n")
    return "".join(out)


def gen_config_c(root, src_file: str) -> str:
    sensors = root.findall("Sensors/Sensor")
    outputs = root.findall("Outputs/Output")
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    out = []
    out.append(HEADER_COMMENT.format(ts=ts, src=src_file))
    out.append('#include "config.h"\n#include "re_types.h"\n\n')

    # Sensor hardware config lookup tables
    out.append(banner("Sensor hardware configuration lookup tables"))
    out.append("typedef struct {\n"
               "    uint8_t              id;\n"
               "    RE_SensorInterface_t iface;\n"
               "    int                  adc_channel;\n"
               "    int                  gpio_pin;\n"
               "    uint32_t             scale;\n"
               "} SensorHWConfig_t;\n\n")

    out.append(f"static const SensorHWConfig_t s_sensor_hw[{len(sensors)}U] = {{\n")
    for s in sensors:
        iface = IFACE_MAP.get(s.get("interface", "ADC"), "RE_SENSOR_IF_ADC")
        adc   = s.get("adc_channel", "-1")
        gpio  = s.get("gpio_pin", "-1")
        scale = s.get("scale_factor", "100")
        out.append(f"    {{ {int(s.get('id')):>3}U, {iface:<22}, {int(adc):>3}, "
                   f"{int(gpio):>3}, {int(scale)}UL }},  /* {s.get('name')} */\n")
    out.append("};\n\n")

    # Output hardware config
    out.append(banner("Output hardware configuration lookup tables"))
    out.append("typedef struct {\n"
               "    uint8_t id;\n"
               "    int     gpio_pin;\n"
               "} OutputHWConfig_t;\n\n")
    out.append(f"static const OutputHWConfig_t s_output_hw[{len(outputs)}U] = {{\n")
    for o in outputs:
        gpio = o.get("gpio_pin", "-1")
        out.append(f"    {{ {int(o.get('id')):>3}U, {int(gpio):>3} }},  /* {o.get('name')} */\n")
    out.append("};\n\n")

    # Helper function implementations
    out.append(banner("HAL lookup function implementations"))
    out.append(
        "RE_SensorInterface_t RE_SensorGetInterface(uint8_t sensor_id)\n{\n"
        f"    uint16_t i;\n"
        f"    for (i = 0U; i < {len(sensors)}U; i++) {{\n"
        "        if (s_sensor_hw[i].id == sensor_id) { return s_sensor_hw[i].iface; }\n"
        "    }\n"
        "    return RE_SENSOR_IF_ADC;\n}\n\n"
    )
    out.append(
        "int RE_SensorGetADCChannel(uint8_t sensor_id)\n{\n"
        f"    uint16_t i;\n"
        f"    for (i = 0U; i < {len(sensors)}U; i++) {{\n"
        "        if (s_sensor_hw[i].id == sensor_id) { return s_sensor_hw[i].adc_channel; }\n"
        "    }\n"
        "    return -1;\n}\n\n"
    )
    out.append(
        "int RE_SensorGetGPIOPin(uint8_t sensor_id)\n{\n"
        f"    uint16_t i;\n"
        f"    for (i = 0U; i < {len(sensors)}U; i++) {{\n"
        "        if (s_sensor_hw[i].id == sensor_id) { return s_sensor_hw[i].gpio_pin; }\n"
        "    }\n"
        "    return -1;\n}\n\n"
    )
    out.append(
        "uint32_t RE_SensorGetScale(uint8_t sensor_id)\n{\n"
        f"    uint16_t i;\n"
        f"    for (i = 0U; i < {len(sensors)}U; i++) {{\n"
        "        if (s_sensor_hw[i].id == sensor_id) { return s_sensor_hw[i].scale; }\n"
        "    }\n"
        "    return 100UL;\n}\n\n"
    )
    out.append(
        "void* RE_SensorGetGPIOPort(uint8_t sensor_id)\n{\n"
        "    (void)sensor_id;\n"
        "    /* Extend for STM32 port objects (GPIOA, GPIOB, ...) */\n"
        "    return (void*)0;\n}\n\n"
    )
    out.append(
        "int RE_OutputGetGPIOPin(uint8_t output_id)\n{\n"
        f"    uint16_t i;\n"
        f"    for (i = 0U; i < {len(outputs)}U; i++) {{\n"
        "        if (s_output_hw[i].id == output_id) { return s_output_hw[i].gpio_pin; }\n"
        "    }\n"
        "    return -1;\n}\n\n"
    )
    out.append(
        "void* RE_OutputGetGPIOPort(uint8_t output_id)\n{\n"
        "    (void)output_id;\n"
        "    return (void*)0;\n}\n"
    )
    return "".join(out)


